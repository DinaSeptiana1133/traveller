      <p class="container">
      Masih ingat dengan gambar di mata uang selebaran 5.000 rupiah zaman dulu? 
      Ya, keindahan Gunung Kelimutu dengan pesona danau tiga warnanya yang mendunia itu bahkan pernah diabadikan dalam mata uang rupiah kita.
      Masuk ke dalam bagian dari Taman Nasional Kelimutu, nama Kelimutu sendiri berasal dari gabungan kata “Keli” yang berarti gunung dan “Mutu” yang berarti mendidih. 
      Gunung Kelimutu memiliki ketinggian 1.639 mdpl atau 5.377 kaki. 
      Gunung Kelimutu mempunyai keindahan yang berbeda dari gunung-gunung yang berada di Indonesia, 
      pasalnya Gunung Kelimutu memiliki Tiga Danau yang terbentuk dari letusan Gunung Kelimutu beberapa tahun silam, dengan warnanya yang berbeda-beda.
      Danau Kelimutu memiliki luas kurang lebih 1.051.000 meter persegi dengan volume air 1.292 juta meter kubik, 
      ketinggian dinding kawah antara 100 hingga 200 meter. 
      Dinding Danau Gunung Kelimutu tergolong terjal dan curam karena memiliki kemiringan 60 sampai 70 derajat. 
      Ketiga  Danau Kawah Gunung Kelimutu memiliki keindahan yang sangat mempesona dan begitu indah sehingga mampu menjadi daya tarik bagi para pengunjung domestik maupun mancanegara. 
      Pengunjung pun rela berjalan kaki menaiki puluhan anak tangga dengan trek yang cukup menanjak, hanya untuk melihat lanskap Danau Kelimutu dari ketinggian. 
      Dari puncak, pengunjung dapat melihat panorama 360 derajat yang mengelilingi Danau Kelimutu. 
      Danau Kelimutu juga merupakan tempat yang tepat untuk menyaksikan panorama keindahan matahari terbit. 
      Pengunjung rela melakukan trekking di pagi buta hanya untuk menyaksikan keindahan sunrise dari puncak Gunung Kelimutu.

