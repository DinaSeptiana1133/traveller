      <p class="container">
      Danau Toba merupakan keajaiban alam menakjubkan di Pulau Sumatera. 
      Sulit membayangkan ada tempat yang lebih indah untuk dikunjungi di Sumatera Utara selain danau ini. 
      Suasana sejuk menyegarkan, hamparan air jernih membiru, dan pemandangan memesona pegunungan hijau adalah sebagian kecil saja dari imaji danau raksasa yang berada 900 meter di atas permukaan laut itu.
      Danau Toba adalah danau berkawah seluas 1.145 kilometer persegi. 
      Di tengahnya berdiam sebuah pulau dengan luas yang hampir sebanding dengan luas negara Singapura. 
      Danau Toba sebenarnya lebih menyerupai lautan daripada danau mengingat ukurannya. 
      Oleh karena itu, Danau Toba ditempatkan sebagai danau terluas di Asia Tenggara dan terbesar kedua di dunia sesudah Danau Victoria di Afrika. 
      Danau Toba juga termasuk danau terdalam di dunia yaitu sekira 450 meter.
      Danau Toba diperkirakan para ahli terbentuk setelah letusan gunung api super sekira 73.000-75.000 tahun lalu. 
      Saat itu 2.800 km kubik bahan vulkanik dimuntahkan Gunung Toba yang meletus hingga debu vulkanik yang ditiup angin menyebar ke separuh wilayah Bumi. 
      Letusannya terjadi selama 1 minggu dan lontaran debunya mencapai 10 kilometer di atas permukaan laut.
      Akibat letusan gunung api super  (Gunung Toba) diperkirakan telah menyebabkan kematian massal dan kepunahan beberapa spesies mahluk hidup. 
      Letusan Gunung Toba telah menyebabkan terjadinya perubahan cuaca bumi dan mulainya masuk ke zaman es sehingga mempengaruhi peradaban dunia.
      Pulau Samosir adalah pulau yang unik karena merupakan pulau vulkanik di tengah Danau Toba. 
      Ketinggiannya 1.000 meter di atas permukaan laut. 
      Meskipun telah menjadi tempat tujuan wisata sejak lama, Samosir merupakan keindahan alam yang belum terjamah. 
      Di tengah Pulau Samosir ini masih ada lagi dua danau indah yang diberi nama Danau Sidihoni dan Danau Aek Natonang. 
      Daerah sekitar Danau Toba memiliki hutan-hutan pinus yang tertata asri. 
      Di pinggiran Danau Toba terdapat beberapa air terjun yang sangat mempesona. 
      Di sekitar Danau Toba juga akan Anda dapati tempat pemandian air belarang.
      Di Pulau Samosir Anda juga dapat menemukan pegunungan berkabut, air terjun yang jernih untuk berenang, dan masyarakat peladang. 
      Keramahan masyarakat Batak pun akan memikat Anda karena kemanapun Anda pergi maka dengan segera dapat menemukan teman baru.
      Di Kota Parapat yang merupakan semenanjung yang menonjol ke danau Anda dapat Anda nikmati pemandangan spektakuler Danau Toba. 
      Parapat dihuni masyarakat Batak Toba dan Batak Simalungan yang dikenal memiliki sifat ceria dan mudah bergaul, 
      terkenal pula senang mendendangkan lagu bertema cinta yang riang namun penuh perasaan.
