      Untuk menarik perhatian dunia, pemerintah tengah membangun resort dan infrastruktur lainnya. 
      Bahkan kecantikan pantai-pantainya nggak kalah menarik dengan yang ada di Bali.
      Lokasi ini juga menjadi tempat sempurna untuk berselancar. Terutama di Pantai Ubrug. 
      Tak hanya punya pemandangan yang begitu menawan, ombak besarnya pun cukup menantang.
      Bagi sebagian wisatawan dalam negeri, nama Mandalika mungkin belum terdengar familiar. 
      Masyarakat lebih mengenal Gili Trawangan, Gili Meno, atau Pantai Senggigi dibandingkan Mandalika. 
      Padahal, kawasan ini tidak kalah cantik dengan destinasi wisata yang sekarang sudah tenar di Lombok.
      Perlu diketahui, KEK, atau Special Economic Zone (SEZ) adalah sebuah wilayah geografis yang memiliki peraturan ekonomi khusus. 
      Peraturan di wilayah KEK biasanya lebih longgar ketimbang wilayah lainnya di suatu negara.
      Kelonggaran didapat terutama untuk perizinan membangun sebuah usaha. 
      Tujuannya untuk menarik minat investasi sebanyak-banyaknya di wilayah tersebut.
      Selain Mandalika, pemerintah Indonesia memiliki 12 KEK, empat di antaranya merupakan KEK Pariwisata, antara lain Tanjung Kelayang, Bangka Belitung, Tanjung Lesung, Banten, dan Morotai, Maluku Utara.
      Nama Mandalika sendiri diambil dari cerita legenda masyarakat Sasak di Lombok tentang Putri Mandalika.
      Ceritanya, Putri Mandalika memilih mengorbankan dirinya ke laut agar tidak terjadi peperangan karena diperebutkan sejumlah pangeran yang hendak meminangnya. 
      Putri itu konon menenggelamkan diri di Pantai Seger. 
      Saat ini, wisatawan bisa melihat monumen Putri Mandalika di pantai itu.
      Berada di lahan seluas 1.034 hektar, KEK Mandalika memiliki lokasi yang sangat strategis. 
      Mandalika berjarak sekitar satu jam perjalanan dari Bandara Internasional Lombok atau setengah jam perjalanan dari pusat kota Lombok.
      Sama seperti Bali yang sering dikunjungi turis mancanegara, Mandalika pun menyediakan banyak pilihan kendaraan yang bisa digunakan menuju ke Mandalika, mulai dari kendaraan umum sampai rental mobil.
      Pantainya yang berpasir putih halus dengan ombak yang menyapu tenang, sangat cocok bagi Anda yang ingin melepaskan penat dari hingar-bingar kemacetan kota besar.
      Di kawasan Mandalika terdapat deretan pantai indah, mulai dari Pantai Tanjung Aan, Pantai Serenting, Pantai Seger, Pantai Kuta, dan Pantai Gerupuk.
      Berbeda dengan Bali, di Mandalika wisatawan akan jarang menemukan bar atau kelab malam. 
      Tak cuma Mandalika, di seluruh pulau nyaris tak bisa ditemukan bar dan kelab malam. 
      Meski demikian, sejumlah tempat makan masih menjual bir untuk mengakmodir keinginan wisatawan mancanegara. 
      Keriaan malam yang seru masih bisa ditemui di sekitaran hotel besar.
      Bagi yang ingin berburu oleh-oleh, Mandalika juga sudah menyediakan sejumlah toko yang berjejer di sepanjang kawasan Pantai Kuta. 
      Pedangan ini menjajakan oleh-oleh khas Lombok, seperti kain tenun, kaus, gelang, hingga ikat kepala.
      Setiap tahunnya di bulan Februari atau Maret, di kawasan Mandalika terdapat acara Festival Bau Nyale. 
      Festival ini berupa tradisi Suku Sasak yang menangkap cacing laut di Pantai Seger.
      Bagi Anda pemburu kuliner, di Kawasan Bukit Mandalika, Pantai Kuta Lombok, Desa Kuta Kecamatan Pujut Lombok Tengah, NTB ada beberapa makanan khas yang patut Anda cicipi. 
      Di antaranya adalah ayam taliwang, ayam Rarang, sate rembiga dan sate bulayak. 
      Olahan bumbu khas daerah Lombok akan memanjakan lidah dan menambah selera makan.
