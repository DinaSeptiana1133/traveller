      <p class="container">
      Kawasan Taman Nasional Ujung Kulon yang memiliki luas kurang lebih 122.956 hektar diresmikan sebagai salah satu Warisan Dunia (Natural World Heritage Site) pada tahun 1992 oleh UNESCO. 
      Pada tahun yang sama kawasan di ujung paling Barat Pulau Jawa ini juga diresmikan menjadi Taman Nasional pertama yang dimiliki Indonesia.
      Taman Nasional Ujung Kulon merupakan rumah bagi populasi Badak Jawa (Badak Bercula Satu) yang saat ini menjadi salah satu satwa mamalia paling langka di muka bumi. 
      Hanya ada puluhan ekor saja Badak yang tersisa di Taman nasional Ujung Kulon saat ini.
      Bukan hanya rumah bagi Badak Jawa bercula satu yang langka, Taman nasional Ujung Kulon juga dihuni hewan lain seperti Macan Tutul, Banteng, Kucing Batu, Rusa, Merak hijau, Babi Hutan, Biawak dan masih banyak lagi. 
      Deretan bermacam tumbuhan langka pun ikut melengkapi ekosistem hutan di Taman nasional Ujung Kulon Banten.
      Ditetapkan sebagai Taman Nasional, tentu saja area ratusan hektar Ujung Kulon tidak diperkenankan untuk dihuni oleh penduduk. 
      Alam yang tumbuh dan berkembang secara alamiah tanpa gangguan tangan manusia ini membentuk Taman Nasional menjadi wilayah yang memiliki keindahan alami.
      Cukup banyak wisatawan yang penasaran dan tertarik ingin menyaksikan dan menikmati langsung keindahan yang dimiliki destinasi wisata Taman nasional Ujung Kulon, yang tentunya diisi dengan aktivitas wisata seru ditengah keindahan alam nya yang mempesona.
      Pulau Peucang adalah salah satu destinasi wisata yang wajib dikunjungi wisatawan saat liburan dan mengisi wisata di Taman Nasional Ujung Kulon Banten.
