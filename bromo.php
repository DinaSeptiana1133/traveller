      <p class="container">
      Gunung Bromo menjadi salah satu destinasi kebanggaan warga Jawa Timur. 
      Eksotisnya pemandangan gunung vulkanik juga bisa membuat Bromo terlihat seperti planet lain.
      Gunung Bromo merupakan salah satu gunung berapi yang masih aktif di Jawa Timur. 
      Gunung yang memiliki ketingggian 2.329 meter di atas permukaan laut ini terdapat dikawasan empat wilayah kabupaten, 
      yaitu Kabupaten Malang, Kabupaten Probolinggo, Kabupaten Pasuruan,dan Kabupaten Lumajang.
      Gunung Bromo mempunyai kawah dengan diameter sekitar 800 meter arah utara-selatan dan sekitar 600 meter arah timur-barat. 
      Daerah bahaya Gunung Bromo berupa lingkaran dengan jari-jari 4 km dari pusat kawah Bromo.
      Gunung Bromo masih satu kawasan dengan Gunung Semeru, yaitu kawasan Taman Nasional Bromo Tengger Semeru yang luas 800 km.
      Sebagai salah satu destinasi wisata, Gunung Bromo ini masih perlu diwaspadai karena statusnya sebagai gunung berapi yang masih aktif. 
      Suhu di sekitar Bromo berkisar antara 3-20 derajat Celcius.
      Sejarahnya, Gunung Bromo sudah mengalami lebih dari 50 erupsi sejak 1804. 
      Karena aktivitas dari gunung vulkanik inilah Bromo punya lanskap yang unik.
      Wisatawan diimbau untuk tidak mendekat sampai radius 1 km. 
      Namun dari jarak aman pun Bromo seperti tak kehilangan pesona. 
      Kawah Bromo yang aktif memberikan pemandangan asap yang tak ada habisnya.
      Pemandangan ini membuat kesan seolah-olah Bromo bukanlah bagian dari Bumi. 
      Kawasan sekitar kawah terlihat gersang dan penuh debu vulkanik membawa imajinasi tentang kehidupan prasejarah yang pernah ada.
      Akibat keindahan yang istimewa itu pula komplek vulkanik Gunung Bromo punya pemandangan eksotis bagaikan sebagai prasejarah, bahkan serasa berada di planet lain.
