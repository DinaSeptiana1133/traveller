      Desa Arborek hanyalah satu diantara desa-desa lain dengan keunikannya masing-masing.
      Berbagai peninggalan sejarah pun banyak terdapat di Kepulauan yang memiliki ikatan dengan kesultanan Tidore, Maluku ini. 
      Mulai dari sebagian penduduknya yang memiliki darah kerajaan Tidore Maluku, peninggalan-peninggalan perang dunia ke 2, sampai gua-gua dengan lukisan tangan khas manusia purba pun tersebar luas di Raja Ampat. 
      Mempelajari keunikan Raja Ampat seolah tidak ada habisnya, inilah yang membuat kepulauan ini begitu berjaya hingga saat ini.
      Betapa kayanya alam, sejarah dan budaya Kepulauan Raja Ampat, sehingga membuat dunia berdecak kagum. 
      Kini Raja Ampat sudah menjadi salah satu tujuan wisata terkenal di dunia, hanya saja karena akses yang terbatas, untuk mencapainya diperlukan biaya yang tidak sedikit.
      Namun, tidak perlu khawatir, Raja Ampat semakin hari semakin berbenah diri dengan menyediakan berbagai fasilitas untuk semua kalangan. 
      Penginapan berbentuk resort dengan harga mahal hingga motel kecil nan murah akan mudah kita jumpai di Raja Ampat. 
