      Hal ini didasarkan pada berbagai penelitian tentang kekayaan flora-fauna dan kelestarian alam laut yang dimiliki Raja Ampat.
      Sebuah laporan badan konservasi internasional pernah menyebutkan bahwa perairan Raja Ampat memiliki sekitar 75% spesies laut seluruh dunia. 
      Bahkan, wilayah laut dan darat Raja Ampat yang memiliki luas 4,6 juta hektar ini menjadi rumah bagi 540 jenis karang, 1.511 spesies ikan dan ribuan biota laut lainnya. 
