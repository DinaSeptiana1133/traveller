      <p class="container">
        Borobudur adalah nama sebuah candi umat Budha yang berbentuk stupa yang didirikan oleh para penganut Budda Mahayana sekitar tahun 800 –an Masehi pada masa pemerintahan Wangsa Syailendra.
        Candi Borobudur merupakan candi atau kuil terbesar didunia yang menjadi monumen Budda terbesar di dunia.
        Para pengikut agama Budda banyak berduyun-duyun datang dari China, Tibet, Kamboja dan India untuk mendapatkan pencerahan di candi Borobudur ini.
        Dinding Candi Borobudur dihiasi oleh pahatan 2672 panel relief yang tersusun sepanjang 6 km yang merupakan relief Budha terbesar dan terlengkap di dunia.
        Relief yang terpahat di dinding tersebut dibagi mejadi 4 kisah : yaitu Karmawibangga, Lalita Wistara, Jataka dan Awanda serta Gandawyuda.
        Relief ini banyak menceritakan tentang perjalanan hidup sang budha dan ajaran-ajarannya serta kemajuan peradaban masyarakat jawa pada masa itu. 
        Terdapat 10 relief kapal yang membuktikan bahwa nenek moyang bangsa Indonesia dulunya adalah pelaut yang tangguh dalam mengarungi samudera.
        Untuk mengikuti cerita dan alur dari relief yang terpahat di dinding candi Borobudur ini, pengunjung harus berjalan searah jarum jam.
        Masuk melalui pintu sebelah timur berjalan searah jarum jam agar posisi candi bisa selalu di sebelah kanan, hingga sampai di tangga timur dan naik ke tingkat berikutnya.
        Berjalan tersebut dilakukan secara berulang ulang hingga semua tingkat dapat terlewati dan sampai ke puncak candi yang berupa induk stupa.
        Menurut prasasti yang bertanggal 26 Mei 824, candi Borobudur ini dibangun oleh raja Samaratungga antara abad ke 8 – 9, bersamaan dengan pembangunan candi Mendut dan Pawon.
        Pembuatan candi Borobudur ini dilakukan selama kurun waktu 75 tahun dipimpin oleh seorang arsitek yang bernama Gunadarma. 
        Meski pada waktu itu teknologi belum maju akan tetapi sudah ada seorang arsitek yang mampu membangun sebuah candi sebanyak 60.000 m <sup>3</sup>.
        Batu andesit yang berjumlah 2 juta balok batu yang didatangkan dari sungai Progo dan sungai Elo yang selanjutnya dirangkai membentuk piramida berundak dengan balok batu yang saling mengunci hingga berbentuk sebuah candi yang sekarang bernama Candi Borobudur yang terletak di sebuah bukit.
        Setelah sampai di puncak, anda tujukan pandangan ke segala arah maka anda akan melihat Gunung Sindoro dan Sumbing, Perbukitan Menoreh, Gunung Merapi dan Gunung Merbabu berdiri megah seakan mengitari candi Borobudur.
        Akhir-akhir ini ada informasi mengenai sejarah candi borobudur yaitu bahwa sesungguhnya candi borobudur merupakan candi peninggalan nabi Sulaiman. 
        Terkait benar atau tidaknya ,candi borobudur merupakan sebuah candi yang harus dikunjungi dan menjadi paket wisata sejarah perjalanan para pelancong di Jogja .
