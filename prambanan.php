      Berbicara Daerah Istimewa Yogyakarta memang tiada habisnya. 
      Selain kota pelajar, kota ini merupakan kota wisata yang selalu menarik perhatian para pelancong. 
      Sebut saja Candi Prambanan, megah, unik, dan merupakan candi Hindu terbesar di Indonesia.
      Terletak di kecamatan Prambanan, Sleman, dan kecamatan Pramabanan, Kalten, kurang lebih 17 kilometer timur laut Yogyakarta. 
      Tepatnya candi ini berada di wilayah administrasi desa Bokoharjo, Prambanan Sleman. 
      Sedangkan pintu masuk kompleks Candi Prambanan terletak di wilayah administrasi desa Tlogo, Prambanan, Klaten.
      Candi Prambanan kerap disebut juga Candi Roro Jonggrang. 
      Konon, candi ini dipersembahkan untuk Trimurti atau tiga dewa utama Hindu, yaitu Brahma (dewa pencipta), Wishnu (dewa pemelihara), dan Siwa (dewa pemusnah).
      Menurut prasasti Siwaghra, nama asli kompleks Candi Prambanan adalah Siwagrha, yang memiliki arti Rumah Siwa. 
      Tepat di ruang utama candi bersemayam arca Siwa Mahadewa. Arca ini punya tinggi 3 meter, menunjukkan bahwa dewa Siwa lebih diutamakan.
      Candi yang masuk dalam situs warisan dunia UNESCO sekaligus salah satu candi terindah di Asia Tenggara ini memiliki arsitektur bangunan berbentuk tinggi dan ramping. 
      Ciri bangunan ini sesuai dengan arsitektur Hindu pada umumnya.
      Kompleks Candi Parambanan punya 4 pintu masuk, yang dibuat mengikuti arah penjuru mata angin, akan tetapi arah hadap bangunan ini ke arah timur. 
      Maka pintu masuk utama candi ini adalah gerbang timur.
      Komplek Candi Prambanan terdiri dari 3 Candi Trimurti (Candi Siwa, Wisnu, dan Brahma), 
      3 Candi Wahana (Candi Nandi, Garuda, dan Angsa), 2 Candi Apit, 4 Candi Kelir, 4 Candi Patok, 
      dan 224 Candi Perwara, yang tersusun dalam 4 barisan konsentris dengan jumlah candi dari barisan terdalam hingga terluar.
